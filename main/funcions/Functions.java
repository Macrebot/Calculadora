package main.funcions;

public class Functions {

    public int Suma(int a, int b){
        return a + b;
    }

    public int Resta (int a, int b){
        return a - b;
    }

    public int Multi (int a, int b){
        return a * b;
    }

    public double Division (int a, int b){
        return (double)a / (double)b;
    }

}
